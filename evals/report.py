"""Generate docs/benchmarks.md from evals/results/*.json."""

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent

HEADER = """# Benchmarks

Auto-generated by ``python -m evals.report`` from ``evals/results/*.json``.
Do not edit by hand. Reproduce any number with::

    uv sync --group evals
    uv run python -m evals.run --task <task>
    uv run python -m evals.report

Every result JSON records the git SHA, the dataset SHA-256 and the full
confusion matrix. Dataset provenance and known biases: ``evals/DATASETS.md``.
The toolbox methods are transparent baselines - they are *expected* to lose
to specialized systems; the point is knowing by how much.
"""


def main() -> int:
    sections = [HEADER]
    for result_file in sorted((ROOT / "results").glob("*.json")):
        data = json.loads(result_file.read_text(encoding="utf-8"))
        sections.append(f"\n## {data['task']}\n")
        sections.append(
            f"Dataset: `{data['dataset']['path']}` (n={data['dataset']['n']}, "
            f"sha256 `{data['dataset']['sha256'][:12]}…`) — "
            f"run at {data['timestamp_utc']}, commit `{data['git_sha'][:10]}`.\n"
        )
        first = next(iter(data["systems"].values()))
        metric_keys = [
            key
            for key, value in first.items()
            if isinstance(value, int | float) and key != "zero_evidence_fraction"
        ]
        sections.append("| System | " + " | ".join(k.replace("_", " ") for k in metric_keys) + " |")
        sections.append("|---|" + "---|" * len(metric_keys))
        for name, scores in data["systems"].items():
            cells = " | ".join(f"{scores[k]:.4f}" for k in metric_keys)
            sections.append(f"| {name} | {cells} |")
        extra = data["systems"].get("toolbox lexicon", {}).get("zero_evidence_fraction")
        if extra is not None:
            sections.append(
                f"\nToolbox lexicon had zero sentiment evidence (no lexicon hit) on "
                f"{extra:.0%} of sentences; those default to a negative prediction."
            )
    out = ROOT.parent / "docs" / "benchmarks.md"
    out.write_text("\n".join(sections) + "\n", encoding="utf-8")
    print(f"wrote {out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
